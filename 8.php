<?php
for($i = 1000; $i < 10000; $i++){

        $a = $i / pow(10, 0) % 10;
        $b = $i / pow(10, 1) % 10;
        $c = $i / pow(10, 2) % 10;
        $d = $i / pow(10, 3) % 10;

        if($a != $b && $a != $c && $a != $d
           && $b != $c && $b != $d && $c != $d)
            echo $i, '|';
}
